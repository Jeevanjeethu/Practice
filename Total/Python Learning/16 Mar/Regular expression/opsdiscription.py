# Operators	Description
# .	 Matches with any single character except newline ‘\n’.

# ?	 match 0 or 1 occurrence of the pattern to its left

# +	 1 or more occurrences of the pattern to its left

# *	 0 or more occurrences of the pattern to its left

# \w	 Matches with a alphanumeric character whereas \W 
# 	(upper case W) matches non alphanumeric character.

# \d	  Matches with digits [0-9] and /D (upper case D) matches with non-digits.

# \s	 Matches with a single white space character (space, newline, return, 
# 	tab, form) and \S (upper case S) matches any non-white space character.

# [..]	 Matches any single character in a square bracket and 
# 		[^..] matches any single character not in square bracket